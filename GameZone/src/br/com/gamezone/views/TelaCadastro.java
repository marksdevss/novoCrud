    private void CarregarColunas() {
        int carregar = tblGames.getSelectedRow();

        if (carregar < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        } else {
            txtID.setText(tblGames.getModel().getValueAt(carregar, 0).toString());
            txtNome.setText(tblGames.getModel().getValueAt(carregar, 1).toString());
            txtDescricao.setText(tblGames.getModel().getValueAt(carregar, 3).toString());
            txtData.setText(tblGames.getModel().getValueAt(carregar, 4).toString());
            txtPreco.setText(tblGames.getModel().getValueAt(carregar, 8).toString());
            txtEstoque.setText(tblGames.getModel().getValueAt(carregar, 9).toString());
        }

    }

