    private void cadastroGames() {

        GameModel gameModel = new GameModel();

        String nome = txtNome.getText();
        String descricao = txtDescricao.getText();
        String dataa = txtData.getText();
        String precoo = txtPreco.getText();
        String estoque = txtEstoque.getText();

        gameModel.setNome(nome);
        gameModel.setDescricao(descricao);
        gameModel.setData_cadastro(dataa);
        gameModel.setPreco(Double.valueOf(precoo));
        gameModel.setEstoque(Integer.parseInt(estoque));

        if (nome.isEmpty() && descricao.isEmpty() && dataa.isEmpty() && estoque.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos vazios ou dados ja existentes");
        } else {

            GameDAO gameDAO = new GameDAO();
            gameDAO.CreateGame(gameModel);

            JOptionPane.showMessageDialog(null, "Jogo cadastrado com sucesso!");
            ListarGames();
        }

    }

    private void ListarGames() {

        try {

            GameDAO gameDAO = new GameDAO();

            DefaultTableModel tableModel = (DefaultTableModel) tblGames.getModel();

            tableModel.setNumRows(0);

            for (GameModel gameModel : gameDAO.ShowGames()) {

                tableModel.addRow(new Object[]{
                    gameModel.getId_jogo(),
                    gameModel.getNome(),
                    gameModel.getDescricao(),
                    gameModel.getData_cadastro(),
                    gameModel.getPreco(),
                    gameModel.getEstoque()
                });

                gameDAO.ShowGames();
                gameDAO.DeleteGame(WIDTH);

            }
        } catch (Exception e) {

            System.out.println("erro: " + e);

        }

    }

    private void AtualizarGame() {

        int id;
        String nome, categorias, descricaoo, data, plataformass, preco, estoque;

        id = Integer.parseInt(txtID.getText());
        nome = txtNome.getText();
        categorias = cbCategorias.getActionCommand();
        descricaoo = txtDescricao.getText();
        data = txtData.getText();
        plataformass = cbPlataformas.getActionCommand();
        preco = txtPreco.getText();
        estoque = txtEstoque.getText();

        GameModel gameModel = new GameModel();

        gameModel.setId_jogo(id);
        gameModel.setNome(nome);
        gameModel.setDescricao(descricaoo);
        gameModel.setData_cadastro(data);
        gameModel.setPreco(Double.valueOf(preco));
        gameModel.setEstoque(Integer.parseInt(estoque));

        GameDAO gameDAO = new GameDAO();

        gameDAO.UpdateGame(gameModel);

    }

    private void ExcluirGame() {

        if (tblGames.getRowSelectionAllowed()) {

            try {

                int id = Integer.parseInt(txtID.getText());

                GameModel gameModel = new GameModel();

                gameModel.setId_jogo(id);

                GameDAO gameDAO = new GameDAO();
                gameDAO.DeleteGame(id);

                JOptionPane.showMessageDialog(null, "Jogo deletado com sucesso!");

                ListarGames();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "voce nao selecionou nenhuma linha, tente novamente!");
            }

        } else {

            JOptionPane.showMessageDialog(null, "Comando cancelado");

        }

    }

